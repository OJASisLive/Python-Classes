# Copying

a = "Om"
b = a      # b is copying the value of a
print("The value of a is: ", a)
print("The value of b is: ", b)

a = "Vino"

print("-----After Changing a-----")

print("The value of a is: ", a)
print("The value of b is: ", b)

# Immutable datatypes are always deep copied
