# Comparision operators

a = 5
b = 6

print(a == b) # Equality comparision (double equal to)

print(a < b) # Less than

print(a != b) # It is the same as (not a == b) [Checks Inequality] (not equal to)

print(a > b) # Greater than

print(a <= b) # Less than or equal to

