# Arithmetic operation (mathematics)
a = 5
b = 6
print(5+6) 
print(a+b) # Addition

print(b-a)
print(a-b) # Subtraction

print(a*b) 
print(b*a) # Multiplication

print(30/5)
print(10/50) # Division (Quotient)

print(29/5)

print(29//5) # Floor division (Simply removes decimal part from the quotient)

print(29/5)
print(29%5) # Modulo (Remainder of a division)

print(5**50) # Exponentation (a**b means a raised to the power of b)
